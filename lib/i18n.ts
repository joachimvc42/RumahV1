/**
 * i18n dictionary for RumahYa.
 * Locales are exposed as URL prefixes: '' (en, default), '/fr', '/es'.
 *
 * To add strings: add to the `en` object first — types propagate so fr/es
 * MUST provide the same keys (TypeScript enforces it).
 */

export const LOCALES = ['en', 'fr', 'es'] as const;
export type Locale = (typeof LOCALES)[number];
export const DEFAULT_LOCALE: Locale = 'en';

export function localeToPrefix(locale: Locale): string {
  return locale === 'en' ? '' : `/${locale}`;
}

export function prefixFor(locale: Locale, path: string = ''): string {
  const p = path.startsWith('/') ? path : `/${path}`;
  if (locale === 'en') return p === '/' ? '/' : p;
  return `/${locale}${p === '/' ? '' : p}`;
}

